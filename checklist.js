function slugify(str) {
  return str
    .toLowerCase()
    .replace(/[^a-zа-яё0-9]+/gi, '-')
    .replace(/^-|-$/g, '');
}

function buildItemId(sectionId, groupTitle, itemIdx) {
  return `${sectionId}__${slugify(groupTitle)}__${itemIdx}`;
}

